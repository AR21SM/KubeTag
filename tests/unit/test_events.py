import json
import tempfile
import pytest
from kubetag.github.events import (
    parse_issue_event,
    parse_issue_payload,
    MalformedEventError,
    IgnoredEventError,
)

def test_valid_event_parsing() -> None:
    payload = {
        "action": "opened",
        "issue": {
            "number": 42,
            "title": "Bug in scheduling",
            "body": "Detailed description here",
            "html_url": "https://github.com/kubernetes/kubernetes/issues/42",
            "user": {"login": "alice", "type": "User"}
        },
        "repository": {
            "name": "kubernetes",
            "owner": {"login": "kubernetes"}
        }
    }
    event = parse_issue_payload(payload)
    assert event.owner == "kubernetes"
    assert event.repo == "kubernetes"
    assert event.issue_number == 42
    assert event.title == "Bug in scheduling"
    assert event.body == "Detailed description here"
    assert event.action == "opened"
    assert event.html_url == "https://github.com/kubernetes/kubernetes/issues/42"

def test_null_body_is_safe() -> None:
    payload = {
        "action": "edited",
        "issue": {
            "number": 100,
            "title": "Title with null body",
            "body": None,
            "html_url": "https://github.com/org/repo/issues/100",
            "user": {"login": "bob", "type": "User"}
        },
        "repository": {
            "name": "repo",
            "owner": {"login": "org"}
        }
    }
    event = parse_issue_payload(payload)
    assert event.body is None

def test_unsupported_action_raises_ignored() -> None:
    payload = {
        "action": "closed",
        "issue": {
            "number": 1,
            "title": "Closed issue",
            "body": "Body",
            "html_url": "url",
            "user": {"type": "User"}
        },
        "repository": {"name": "repo", "owner": {"login": "org"}}
    }
    with pytest.raises(IgnoredEventError, match="Ignoring unsupported action"):
        parse_issue_payload(payload)

def test_pull_request_raises_ignored() -> None:
    payload = {
        "action": "opened",
        "issue": {
            "number": 2,
            "title": "PR Title",
            "body": "PR Body",
            "html_url": "url",
            "pull_request": {},  # Denotes a PR
            "user": {"type": "User"}
        },
        "repository": {"name": "repo", "owner": {"login": "org"}}
    }
    with pytest.raises(IgnoredEventError, match="Ignoring event because it is a pull request"):
        parse_issue_payload(payload)

