def test_malformed_event_missing_fields() -> None:
    payload = {
        "action": "opened",
        # missing issue
        "repository": {"name": "repo", "owner": {"login": "org"}}
    }
    with pytest.raises(MalformedEventError, match="missing 'issue' object"):
        parse_issue_payload(payload)

def test_parse_issue_event_from_file() -> None:
    payload = {
        "action": "reopened",
        "issue": {
            "number": 99,
            "title": "Reopened Issue",
            "body": "Body text",
            "html_url": "url",
            "user": {"type": "User"}
        },
        "repository": {"name": "repo", "owner": {"login": "org"}}
    }
    
    with tempfile.NamedTemporaryFile(mode="w", suffix=".json", delete=False, encoding="utf-8") as tmp:
        json.dump(payload, tmp)
        tmp_name = tmp.name

    try:
        event = parse_issue_event(tmp_name)
        assert event.issue_number == 99
        assert event.action == "reopened"
    finally:
        import os
        os.unlink(tmp_name)

def test_parse_issue_event_nonexistent_file() -> None:
    with pytest.raises(MalformedEventError, match="Event file not found"):
        parse_issue_event("/nonexistent/file/path.json")
